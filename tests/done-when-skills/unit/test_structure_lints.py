def test_no_file_in_plugin_claims_persona_judge_consumes_fitness_rubrics():
    # Walk every file under plugin dir, ensure none asserts persona-judge consumes our rubrics
    for path in PLUGIN.rglob("*.md"):
        text = path.read_text(encoding="utf-8")
        # banned phrasings
        for phrase in [
            "persona-judge skill can consume",
            "persona-judge consumes",
            "consumable by the persona-judge",
            "persona-judge from persona-distill consumes",
        ]:
            assert phrase not in text, f"{path}: bans phrase {phrase!r}"
    for path in PLUGIN.rglob("*.yaml"):
        text = path.read_text(encoding="utf-8")
        # the schema itself may MENTION persona-judge to warn against it; that's fine.
        # what we ban is the actual `judge: persona-judge` value in templated YAML.
        # The bare schema doc files (under references/) include illustrative examples
        # in the WARNING text — exempt those.
        if "done-when-schema.yaml" in str(path):
            continue
        if "done-when-template.yaml" in str(path):
            continue
        assert "judge: persona-judge" not in text, f"{path}: still uses judge: persona-judge"


def test_no_invented_personas_referenced_anywhere_in_plugin():
    # The three invented personas must be removed (or only mentioned as 'this was wrong')
    invented = ["integration-engineer-persona", "non-technical-end-user-persona", "oncall-sre-persona"]
    bad_hits = []
    for path in list(PLUGIN.rglob("*.md")) + list(PLUGIN.rglob("*.yaml")) + list(PLUGIN.rglob("*.json")):
        text = path.read_text(encoding="utf-8")
        for p in invented:
            if p in text:
                # allow if surrounded by "wrong" / "error" / "removed" / "earlier draft" markers
                # context window of ±200 chars
                idx = text.find(p)
                ctx = text[max(0, idx - 200): idx + len(p) + 200].lower()
                if any(marker in ctx for marker in ["wrong", "error", "removed", "earlier draft", "do not", "invented", "category error"]):
                    continue
                bad_hits.append(f"{path}: {p}")
    assert not bad_hits, "persona references not removed:\n  " + "\n  ".join(bad_hits)


