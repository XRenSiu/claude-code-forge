def test_subscription_example_glossary_terms_referenced_in_spec_body():
    """Every Glossary term should be used in the spec body — sanity check."""
    base = PLUGIN / "skills/acceptance-spec/references/examples/subscription-cancellation"
    spec_text = (base / "spec.md").read_text()
    # extract terms from "- **<term>**" lines under ## Glossary
    glossary_start = spec_text.find("## Glossary")
    assert glossary_start > 0, "no Glossary section"
    glossary = spec_text[glossary_start:]
    terms = re.findall(r"-\s+\*\*([^*]+)\*\*", glossary)
    body = spec_text[:glossary_start]
    missing = [t for t in terms if t not in body]
    assert not missing, f"glossary terms not used in spec body: {missing}"


# ---------- runner ----------
def _run():
    failed = []
    passed = 0
    for name, fn in sorted(globals().items()):
        if name.startswith("test_") and callable(fn):
            try:
                fn()
                print(f"✓ {name}")
                passed += 1
            except AssertionError as e:
                print(f"✗ {name}: {e}", file=sys.stderr)
                failed.append(name)
            except Exception as e:
                print(f"✗ {name}: ERROR {type(e).__name__}: {e}", file=sys.stderr)
                failed.append(name)
    print("─────────────────────────────────")
    print(f"Structure lints: {passed} passed · {len(failed)} failed")
    if failed:
        print("Failed:")
        for n in failed:
            print(f"  - {n}")
        sys.exit(1)


if __name__ == "__main__":
    _run()
