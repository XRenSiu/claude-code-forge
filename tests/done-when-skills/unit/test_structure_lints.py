def test_every_reference_path_mentioned_in_skillmd_exists_on_disk():
    """Scan both SKILL.md files for references/... paths and check each."""
    bad = []
    for skill_path in [
        PLUGIN / "skills/acceptance-spec/SKILL.md",
        PLUGIN / "skills/test-suite-generator/SKILL.md",
    ]:
        text = skill_path.read_text(encoding="utf-8")
        # backtick-quoted paths like `references/foo.md`
        for m in re.finditer(r"`(references/[^`\s]+)`", text):
            ref = m.group(1)
            target = skill_path.parent / ref
            if not target.exists():
                bad.append(f"{skill_path.name}: references {ref} (resolved: {target}) — MISSING")
    assert not bad, "broken reference paths:\n  " + "\n  ".join(bad)


